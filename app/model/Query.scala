  val lshs = new LSHStructure(Array(new HashTable(() => new Hyperplane(5, () => new Random(System.currentTimeMillis())))))

  // return the result generated by
  def getResults(imageId:String, range:Float) : (String, List[((String, Array[Float]), Float)]) = {
    val fVector = lshs.findVectorById(imageId)
    val tres = time {
      lshs.query(fVector, range, Cosine)
    }
    val res2 = tres._1.zip(tres._1.map(x => Cosine.measure(fVector._2, x._2))).sortBy(_._2)
    (tres._2, res2.toList)
  }

  def time[R](block: => R) = {
    val t0 = System.currentTimeMillis()
    val result = block    // call-by-name
    val t1 = System.currentTimeMillis()
    (result, (t1 - t0) + "ms")
  }

}
